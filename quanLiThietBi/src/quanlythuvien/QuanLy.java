    private void jButton_checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_checkActionPerformed
        ConnectDB cn = new ConnectDB();
        Connection conn = cn.getConnectDB();
        
        try {
                            
                            Statement stS = conn.createStatement();
                            ResultSet rsS = stS.executeQuery("SELECT TENSACH,TACGIA,THELOAI,NHA_XB FROM dbo.SACH WHERE dbo.SACH.MASACH = '" + jTextField_ctmasach.getText() +"'");
                            while(rsS.next()){
                                String tensach = rsS.getNString("TENSACH");
                                String tacgia = rsS.getNString("TACGIA");
                                String theloai = rsS.getNString("THELOAI");
                                String nxb = rsS.getNString("NHA_XB");
                                
                                
                                jTextField_cttensach.setText(tensach);
                                jTextField_cttentacgia.setText(tacgia);
                                jTextField_cttheloai.setText(theloai);
                                jTextField_ctnxb.setText(nxb);
                            }
                            
                        } catch (SQLException ex) {
                            Logger.getLogger(QuanLy.class.getName()).log(Level.SEVERE, null, ex);
                        }
    }//GEN-LAST:event_jButton_checkActionPerformed

    private void jButton_tkdocgiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_tkdocgiaActionPerformed
        tbn_timkiemDG.setRowCount(0);
        if(jTextField_tkdocgia.getText().length() != 0){
            if (jComboBox_tkdocgia.getSelectedItem().toString() == "Mã độc giả") {
                String tukhoa = jTextField_tkdocgia.getText();
                String query = "SELECT * FROM DOCGIA "
                        + "WHERE MADOCGIA LIKE '%"+tukhoa+"%'";                       
                loadDataToTimkiemDocGia(query);
            } 
            else if(jComboBox_tkdocgia.getSelectedItem().toString() == "Tên độc giả"){
                String tukhoa = jTextField_tkdocgia.getText();
                String query = "SELECT * FROM DOCGIA "
                        + "WHERE TENDOCGIA LIKE N'%"+tukhoa+"%'";                       
                loadDataToTimkiemDocGia(query);
            }
            else if(jComboBox_tkdocgia.getSelectedItem().toString() == "Quê quán"){
                String tukhoa = jTextField_tkdocgia.getText();
                String query = "SELECT * FROM DOCGIA "
                        + "WHERE QUEQUAN LIKE N'%"+tukhoa+"%'";                       
                loadDataToTimkiemDocGia(query);
            }
            
            else JOptionPane.showMessageDialog(rootPane, "Không tìm thấy sách yêu cầu");
        }
        else JOptionPane.showMessageDialog(rootPane, "Bạn cần nhập thông tin để tìm kiếm!");
    }//GEN-LAST:event_jButton_tkdocgiaActionPerformed

