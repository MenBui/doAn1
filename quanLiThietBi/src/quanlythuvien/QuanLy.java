    private void jButton_importSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_importSachActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        int result = file.showOpenDialog(null);
        String excelFilePath = null;
        if(result == JFileChooser.APPROVE_OPTION){
            excelFilePath = file.getSelectedFile().getAbsolutePath();
        }
        ReadSachFromExcel reader = new ReadSachFromExcel();
        try {
            List<Sach> listSachs = reader.readSachsFromExcelFile(excelFilePath);
            SachDAO sachdao = new SachDAO();
            sachdao.insertSachs(listSachs);
            loadDataToTableSach();
        } catch (IOException ex) {
            Logger.getLogger(QuanLy.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_importSachActionPerformed

